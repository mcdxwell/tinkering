package main

import "fmt"

func main() {

	fmt.Println("Hello, \nGo!")
	// New lines
	// Output:
	// Hello,
	// Go!

	fmt.Println("Hello, \tGo!")
	// Tabs
	// Output:
	// Hello,	Go!

	fmt.Println("Quotes \"\"")
	// Quoting
	// Output:
	// Quotes: ""

	fmt.Println("Backslash: \\")
	// Backslashing
	// Output:
	// Backslash: \
}
